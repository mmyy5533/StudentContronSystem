package com.itheima.arithmeticoperator;

public class 自增运算符 {
    public static void main(String[] args) {
        //  ++ 和 --
        int a = 10;
        a++;
        System.out.println(a);
        ++a;
        System.out.println(a);
        a--;
        System.out.println(a);
        --a;
        System.out.println(a);

    }
}
