package com.itheima.LoopDemo;

public class ForDemo1 {
    public static void main(String[] args){
        // 需求：打印5次Hello World

        //分析
        //i 1~5
        /*for(初始化语句;条件判断语句;条件控制语句){
            循环体语句};
         */
        for (int i = 5; i >= 1; i--){
            System.out.println(i);
        }
    }
}
